// comparision operator

// equal operator
const isEqual = 10 == 10;
console.log(isEqual);

//  not equal operator
const NotisEqual = 10 != 10;
console.log(NotisEqual);

//  greater than operator
const Greaterthan = 10 > 5;
console.log(Greaterthan);

// lessthan operator
const Lessthan = 10 < 5;
console.log(Lessthan);
